import kotlinx.datetime.Clock
import kotlinx.datetime.LocalDate
import kotlinx.serialization.Contextual
import kotlinx.serialization.Serializable
import org.delcom.entities.Match

@Serializable
data class MatchRequest(
    var userId: String = "",

    var opponent: String = "",
    var venue: String = "",
    var sport: String = "football",

    @Contextual
    var matchDate: LocalDate = LocalDate(2000, 1, 1),

    var myScore: Int = 0,
    var opponentScore: Int = 0,

    // ── Tim Sendiri ────────────────────────────────────────────────────────
    var goals: Int = 0,
    var assists: Int = 0,
    var shots: Int = 0,
    var shotsOnTarget: Int = 0,
    var bigChances: Int = 0,
    var bigChancesMissed: Int = 0,
    var tackles: Int = 0,
    var interceptions: Int = 0,
    var clearances: Int = 0,
    var saves: Int = 0,
    var blockedShots: Int = 0,
    var fouls: Int = 0,
    var yellowCards: Int = 0,
    var redCards: Int = 0,
    var offsides: Int = 0,
    var possession: Int = 0,
    var passAccuracy: Int = 0,
    var totalPasses: Int = 0,
    var corners: Int = 0,
    var dribbles: Int = 0,
    var aerialDuelsWon: Int = 0,
    var rebounds: Int = 0,
    var steals: Int = 0,
    var blocks: Int = 0,
    var turnovers: Int = 0,
    var threePointers: Int = 0,

    // ── Statistik Lawan ────────────────────────────────────────────────────
    var oppGoals: Int = 0,
    var oppAssists: Int = 0,
    var oppShots: Int = 0,
    var oppShotsOnTarget: Int = 0,
    var oppBigChances: Int = 0,
    var oppBigChancesMissed: Int = 0,
    var oppTackles: Int = 0,
    var oppInterceptions: Int = 0,
    var oppClearances: Int = 0,
    var oppSaves: Int = 0,
    var oppBlockedShots: Int = 0,
    var oppFouls: Int = 0,
    var oppYellowCards: Int = 0,
    var oppRedCards: Int = 0,
    var oppOffsides: Int = 0,
    var oppPossession: Int = 0,
    var oppPassAccuracy: Int = 0,
    var oppTotalPasses: Int = 0,
    var oppCorners: Int = 0,
    var oppDribbles: Int = 0,
    var oppAerialDuelsWon: Int = 0,
    var oppRebounds: Int = 0,
    var oppSteals: Int = 0,
    var oppBlocks: Int = 0,
    var oppTurnovers: Int = 0,
    var oppThreePointers: Int = 0,

    var notes: String = "",
) {
    fun toMap(): Map<String, Any?> = mapOf(
        "userId"              to userId,
        "opponent"            to opponent,
        "venue"               to venue,
        "sport"               to sport,
        "matchDate"           to matchDate.toString(),
        "myScore"             to myScore,
        "opponentScore"       to opponentScore,
        "goals"               to goals,
        "assists"             to assists,
        "shots"               to shots,
        "shotsOnTarget"       to shotsOnTarget,
        "bigChances"          to bigChances,
        "bigChancesMissed"    to bigChancesMissed,
        "tackles"             to tackles,
        "interceptions"       to interceptions,
        "clearances"          to clearances,
        "saves"               to saves,
        "blockedShots"        to blockedShots,
        "fouls"               to fouls,
        "yellowCards"         to yellowCards,
        "redCards"            to redCards,
        "offsides"            to offsides,
        "possession"          to possession,
        "passAccuracy"        to passAccuracy,
        "totalPasses"         to totalPasses,
        "corners"             to corners,
        "dribbles"            to dribbles,
        "aerialDuelsWon"      to aerialDuelsWon,
        "rebounds"            to rebounds,
        "steals"              to steals,
        "blocks"              to blocks,
        "turnovers"           to turnovers,
        "threePointers"       to threePointers,
        "oppGoals"            to oppGoals,
        "oppAssists"          to oppAssists,
        "oppShots"            to oppShots,
        "oppShotsOnTarget"    to oppShotsOnTarget,
        "oppBigChances"       to oppBigChances,
        "oppBigChancesMissed" to oppBigChancesMissed,
        "oppTackles"          to oppTackles,
        "oppInterceptions"    to oppInterceptions,
        "oppClearances"       to oppClearances,
        "oppSaves"            to oppSaves,
        "oppBlockedShots"     to oppBlockedShots,
        "oppFouls"            to oppFouls,
        "oppYellowCards"      to oppYellowCards,
        "oppRedCards"         to oppRedCards,
        "oppOffsides"         to oppOffsides,
        "oppPossession"       to oppPossession,
        "oppPassAccuracy"     to oppPassAccuracy,
        "oppTotalPasses"      to oppTotalPasses,
        "oppCorners"          to oppCorners,
        "oppDribbles"         to oppDribbles,
        "oppAerialDuelsWon"   to oppAerialDuelsWon,
        "oppRebounds"         to oppRebounds,
        "oppSteals"           to oppSteals,
        "oppBlocks"           to oppBlocks,
        "oppTurnovers"        to oppTurnovers,
        "oppThreePointers"    to oppThreePointers,
        "notes"               to notes,
    )

    fun toEntity(): Match = Match(
        userId           = userId,
        opponent         = opponent,
        venue            = venue,
        sport            = sport,
        matchDate        = matchDate,
        myScore          = myScore,
        opponentScore    = opponentScore,
        goals            = goals,
        assists          = assists,
        shots            = shots,
        shotsOnTarget    = shotsOnTarget,
        bigChances       = bigChances,
        bigChancesMissed = bigChancesMissed,
        tackles          = tackles,
        interceptions    = interceptions,
        clearances       = clearances,
        saves            = saves,
        blockedShots     = blockedShots,
        fouls            = fouls,
        yellowCards      = yellowCards,
        redCards         = redCards,
        offsides         = offsides,
        possession       = possession,
        passAccuracy     = passAccuracy,
        totalPasses      = totalPasses,
        corners          = corners,
        dribbles         = dribbles,
        aerialDuelsWon   = aerialDuelsWon,
        rebounds         = rebounds,
        steals           = steals,
        blocks           = blocks,
        turnovers        = turnovers,
        threePointers    = threePointers,
        oppGoals         = oppGoals,
        oppAssists       = oppAssists,
        oppShots         = oppShots,
        oppShotsOnTarget = oppShotsOnTarget,
        oppBigChances    = oppBigChances,
        oppBigChancesMissed = oppBigChancesMissed,
        oppTackles       = oppTackles,
        oppInterceptions = oppInterceptions,
        oppClearances    = oppClearances,
        oppSaves         = oppSaves,
        oppBlockedShots  = oppBlockedShots,
        oppFouls         = oppFouls,
        oppYellowCards   = oppYellowCards,
        oppRedCards      = oppRedCards,
        oppOffsides      = oppOffsides,
        oppPossession    = oppPossession,
        oppPassAccuracy  = oppPassAccuracy,
        oppTotalPasses   = oppTotalPasses,
        oppCorners       = oppCorners,
        oppDribbles      = oppDribbles,
        oppAerialDuelsWon = oppAerialDuelsWon,
        oppRebounds      = oppRebounds,
        oppSteals        = oppSteals,
        oppBlocks        = oppBlocks,
        oppTurnovers     = oppTurnovers,
        oppThreePointers = oppThreePointers,
        notes            = notes,
        updatedAt        = Clock.System.now(),
    ).calculateResult()
}